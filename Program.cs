Console.WriteLine("Even And Odd Numbers");
for (int i = 1; i < 100; i++)
{
    if (i % 2 == 0)
    {
        Console.WriteLine("{0} IS EVEN NUMBER ", i);

    }
    else
    {
        Console.WriteLine("{0} is ODD NUMBER", i);
    }


}


